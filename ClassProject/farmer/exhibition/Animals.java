package exhibition;

public class Animals {
	
    private String name;
    
    public Animals (String name){
    	this.name = name;
    }
	
	public String eat() {
		return "I am a "+name+" and I can eat";
	}
	
	public String run() {
		return "I am a "+name+" and I can run";
	}
	
	public String walk() {
		return "I am a "+name+" and I can walk";
	}
	
	public String jump() {
		return "I am a "+name+" and I can jump";
	}
	
}
	
	


